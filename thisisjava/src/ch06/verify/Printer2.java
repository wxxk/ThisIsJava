package ch06.verify;

public class Printer2 {
	static void println(int printer) {
		System.out.println(printer);
	}
	
	static void println(boolean printer) {
		System.out.println(printer);
	}
	
	static void println(double printer) {
		System.out.println(printer);
	}
	
	static void println(String printer) {
		System.out.println(printer);
	}
}
