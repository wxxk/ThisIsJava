package ch06.verify;

public class Printer {
	void println (int printer) {
		System.out.println(printer);
	}
	
	void println (boolean printer) {
		System.out.println(printer);
	}
	
	void println (double printer) {
		System.out.println(printer);
	}
	
	void println (String printer) {
		System.out.println(printer);
	}
}
